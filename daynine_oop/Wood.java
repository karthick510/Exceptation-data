package com.onesoft.daynine_oop;

public class Wood {
	private String brand;
	private int price;
	private int weight;
	
	public void setBrand (String brand) {
		this.brand=brand;
	}
	public String getBrand () {
		return brand;
	}
	public void setPrice (int p) {
		price=p;
	}
	public int getPrice() {
		return price;
	}
	public void setWeight (int w) {
		weight=w;
	}
	public int getWeight () {
		return weight;
		
	
		
	}
}
