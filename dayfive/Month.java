package com.onesoft.dayfive;

public class Month {
	public static void main (String [] args) {
		String month="september";
		switch (month) {
		case "january":System.out.println("Frist month of the year");break;
		case "feburary":System.out.println("Second month of the year");break;
		case "march":System.out.println("Third month of the year");break;
		case "april":System.out.println("Fourth month of the year");break;
		case "may":System.out.println("Fifth month of the year");break;
		case "june":System.out.println("Sixth month of the year");break;
		case "july":System.out.println("Seventh month of the year");break;
		case "august":System.out.println("Eight month of the year");break;
		case "september":System.out.println("Ninth month of the year");break;
		case "octomber":System.out.println("Tenth month of the year");break;
		case "november":System.out.println("Eleventh month of the year");break;
		case "december":System.out.println("Tweleth month of the year");break;
		default:System.out.println("Invalid");break;
	}
	}
}
